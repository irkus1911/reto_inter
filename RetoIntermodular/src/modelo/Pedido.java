package modelo;

public class Pedido extends Compra {

	private boolean estado;
	// estado true -> Pedido Aceptado y volcado a Historico
	// estado false -> Pedido pendente de Validar

	public boolean isEstado() {
		return estado;
	}

	public void setEstado(boolean estado) {
		this.estado = estado;
	}
	
}
